public class Counter {

    static int count = 1200;

    public static void main(String[] args) {

        System.out.println("count / 10 === " + count/10);
        System.out.println("count / 20 === " + count/20);
        System.out.println("count / 30 === " + count/30);
        System.out.println("count / 40 === " + count/40);
        System.out.println("count / 50 === " + count/50);

    }
}
